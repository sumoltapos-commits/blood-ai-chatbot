API_KEY = st.secrets["GEMINI_API_KEY"]
genai.configure(api_key=API_KEY)

# ==========================================
# 2. อัปโหลดไฟล์คู่มือเข้าสู่ "สมอง" ของ AI
# ==========================================
print("⏳ กำลังอัปโหลดไฟล์คู่มือเข้าสู่ระบบความจำของ AI (รอสักครู่นะครับ)...")
# โค้ดนี้จะส่งไฟล์ PDF ไปเก็บไว้ในเซิร์ฟเวอร์ของ Google ชั่วคราวเพื่อให้ AI อ่าน
blood_document = genai.upload_file(path="คู่มือการรับบริจาคโลหิต-Blood-Donation-Manual-ปี-2564", display_name="Blood_Donation_Guideline")
print(f"✅ อัปโหลดเสร็จแล้ว! (ชื่ออ้างอิง: {blood_document.display_name})")

# รอให้ AI ประมวลผลและอ่านไฟล์ให้จบก่อน (สำคัญมาก ไม่งั้นมันจะบอกว่าหาไฟล์ไม่เจอ)
print("🧠 กำลังให้ AI อ่านหนังสือทั้งเล่ม รอประมาณ 10 วินาที...")
time.sleep(10)

# ==========================================
# 3. ตั้งค่า AI รุ่นล่าสุด + เปิดโหมดค้นหาเว็บ (Web Grounding)
# ==========================================
# เปิดใช้งานเครื่องมือค้นหา Google Search ควบคู่ไปด้วย
model = genai.GenerativeModel(
    model_name='gemini-2.5-flash',
    tools=[{"google_search_retrieval": {}}] # สวิตช์เปิดการค้นหาบนอินเทอร์เน็ต
)

# ==========================================
# 4. สั่งเริ่มแชท พร้อมป้อนคำสั่งตั้งต้น (System Prompt)
# ==========================================
chat = model.start_chat(history=[
    {
        "role": "user", 
        "parts": [
            blood_document, 
            "นี่คือคู่มือการรับบริจาคโลหิต คุณคือผู้เชี่ยวชาญด้านนี้ เวลาตอบคำถามให้ค้นหาข้อมูลจากในไฟล์นี้ก่อนเสมอ หากเรื่องไหนไม่มีในไฟล์ หรือต้องการข้อมูลที่อัปเดตล่าสุด ให้คุณใช้เครื่องมือค้นหาจากอินเทอร์เน็ต (Google Search) เพื่อหาคำตอบเพิ่มเติม ตอบเป็นภาษาไทยให้อ่านง่ายและเป็นกันเอง"
        ]
    }
])

print("\n===========================================")
print("🩸 ยินดีต้อนรับสู่ AI ที่ปรึกษาการบริจาคโลหิต!")
print("พิมพ์คำถามของคุณได้เลย (หากต้องการเลิกคุย ให้พิมพ์คำว่า 'ออก')")
print("===========================================\n")

# ==========================================
# 5. ระบบถาม-ตอบ (Chat Loop)
# ==========================================
