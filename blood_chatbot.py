    user_text = input("🙋 คุณ: ")
    
    # ถ้าพิมพ์คำว่า 'ออก' ให้ปิดโปรแกรม
    if user_text.strip() == 'ออก':
        print("🤖 AI: ยินดีที่ได้ให้คำปรึกษาครับ ไว้คุยกันใหม่นะครับ!")
        break
        
